package week3.day4;

public class ArrayBasics {

	public static void main(String[] args) {
		// similar types of multiple values
		int num[]; // declare
		num=new int[5]; // initialize
		
		num[0]=9;
		num[1]=6;
		num[2]=3;
		num[3]=8;
		num[4]=5;
		
		//value access
		System.out.println(num[0]+","+num[1]+","+num[2]+","+num[3]+","+num[4]+",");
		System.out.println(num.length);// length of an array
		
		
		

	}

}
